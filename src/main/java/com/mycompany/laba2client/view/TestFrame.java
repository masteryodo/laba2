    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Удаление клиента
        int row = jTable1.getSelectedRow();
        Client client = getClientById((Long) clients_model.getValueAt(row, 0));
        try {
            controller.sendClientUpdates("remove", client);
            jTable1.removeRowSelectionInterval(row, row);
            refreshClientsTableModel();
        } catch (Exception ex) {
            Logger.getLogger(TestFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Удалить ордер
        Order order = getOrderById((Long) orders_model.getValueAt(jTable2.getSelectedRow(), 0));
        try {
            controller.sendOrderUpdates("remove", order);
            refreshOrdersTableModel();
        } catch (Exception e) {
            System.out.println("remove action: " + e );
        }
    }//GEN-LAST:event_jButton5ActionPerformed

